package com.prasanna.entity;

public class TestServer {
	public String date;
	
	public String buildNumber;
	
	public String status;
	
	public String lastCheckIn;
	
	public String taskName;
	
    public String changesDone;
    
    public String codeDone;
    
    public String summary;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getBuildNumber() {
		return buildNumber;
	}

	public void setBuildNumber(String buildNumber) {
		this.buildNumber = buildNumber;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLastCheckIn() {
		return lastCheckIn;
	}

	public void setLastCheckIn(String lastCheckIn) {
		this.lastCheckIn = lastCheckIn;
	}

	public String getTaskName() {
		return taskName;
	}

	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}

	public String getChangesDone() {
		return changesDone;
	}

	public void setChangesDone(String changesDone) {
		this.changesDone = changesDone;
	}

	public String getCodeDone() {
		return codeDone;
	}

	public void setCodeDone(String codeDone) {
		this.codeDone = codeDone;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}
}
